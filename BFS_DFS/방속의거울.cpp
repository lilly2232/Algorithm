// 아래 기본 제공된 코드를 수정 또는 삭제하고 본인이 코드를 사용하셔도 됩니다.
#include <cstdio>
#include <iostream>
#define MAX 1002
#define INF 987654321
using namespace std;
char arr[MAX][MAX];
int visit[MAX][MAX];
int num;
int cnt = 0;

int sol(int x, int y, int dir)
{
	if (visit[x][y]== 0) {
		visit[x][y] = 1;
	}
	
	
	if (arr[x][y] == '2') {
		if (dir == 1) {
			for (int i = x + 1; i < num; i++)
				if (arr[i][y] - 48 > 0) {
					if (visit[i][y] == 0)
						return  1 + sol(i, y, 4);
					else
						return sol(i, y, 4);
				}
		}
		else if (dir == 2) {
			for (int i = x - 1; i > -1; i--)
				if (arr[i][y] - 48 > 0) {
					if (visit[i][y] == 0)
						return  1 + sol(i, y, 3);
					else
						return sol(i, y, 3);
				}
		}
		else if (dir == 3) {
			for (int i = y - 1; i > -1; i--)
				if (arr[x][i] - 48 > 0) {
					if (visit[x][i] == 0)
						return 1 + sol(x, i, 2);
					else
						return  sol(x, i, 2);
				}
		}
		else if (dir == 4) {
			for (int i = y + 1; i < num; i++)
				if (arr[x][i] - 48 > 0) {
					if (visit[x][i] == 0)
						return  1 + sol(x, i, 1);
					else
						return  sol(x, i, 1);
				}
		}
	}
	if (arr[x][y] == '1') {
		if (dir == 1) {
			for (int i = x - 1; i > -1; i--)
				if (arr[i][y] - 48 > 0) {
					if (visit[i][y] == 0)
						return 1 + sol(i, y, 3);
					else
						return  sol(i, y, 3);
				}

		}
		else if (dir == 2) {
			for (int i = x + 1; i < num; i++)
				if (arr[i][y] - 48 > 0) {
					if (visit[i][y] == 0)
						return  1 + sol(i, y, 4);
					else
						return  sol(i, y, 4);

				}
		}
		else if (dir == 3) {
			for (int i = y + 1; i < num; i++)
				if (arr[x][i] - 48 > 0) {
					if (visit[x][i] == 0)
						return  1 + sol(x, i, 1);
					else
						return  sol(x, i, 1);
				}
		}
		else if (dir == 4) {
			for (int i = y - 1; i > -1; i--)
				if (arr[x][i] - 48 > 0) {
					if (visit[x][i] == 0)
						return  1 + sol(x, i, 2);
					else
						return  sol(x, i, 2);
				}
		}
	}
	return 0;
}
